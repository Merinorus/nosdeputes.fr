<a name="scrutins"></a><h2>Participation aux scrutins solennels</h2>
<h3>Résumé</h3>
<ul><li><strong>39 députés sanctionnables</strong> encourant une perte de 4&nbsp;238,91&nbsp;€ à 8&nbsp;477,82&nbsp;€</li>
<li>La somme de ces sanctions représenterait <strong>190&nbsp;750,95&nbsp;€ d'économies</strong> pour l'Assemblée</li>
</ul>
<h3>Règlement</h3>
<p><a href="http://www.assemblee-nationale.fr/connaissance/reglement.asp#P2011_283477">L'article 159 du règlement</a> prévoit une retenue sur indemnité pour les députés qui ne prennent pas suffisamment part aux scrutins solennels.</p>
<ul>
        <li>Si un député n'assiste pas à au moins 2/3 des scrutins solennels, il perd alors 1/3 de ses indemnités de fonction sur la session parlementaire ordinaire, soit 4&nbsp;238,91&nbsp;€&nbsp;;</li>
        <li>Si le député assiste à moins de la moitié de ces scrutins, 2/3 de ses indemnités de fonction sur la session parlementaire ordinaire lui sont alors retirées soit 8&nbsp;477,82&nbsp;€.</li>
</ul>
<p>L'indemnité de fonction d'un député s'élève à 1&nbsp;412,97&nbsp;€ par mois depuis le 1er octobre 2009. La session parlementaire ordinaire se déroule du 1er octobre au 30 juin (9 mois).</p>
<p>Les députés absents peuvent se faire représenter par un collègue via une délégation de vote ou se faire excuser. Selon nos informations, le mécanisme d'excuses n'est pas utilisé. Ces excuses ne sont dans tous les cas pas publicisées.</p>
<p>Cette règle existait avant octobre 2009, date d'application du nouveau règlement.</p>
<p>Même si selon nos informations, les excuses pour les scrutins publics ne sont pas utilisées et les sanctions pas appliquées, nous n'en avons pas la preuve. Nous ne pouvons pas publier la liste des députés sanctionnables. Quiconque souhaitant pousser plus loin l'étude sur ce point spécifique du règlement peut retravailler à partir de nos données téléchargeables <a href="http://www.regardscitoyens.org/telechargement/confiseurs/scrutins-solennels.csv">en cliquant sur ce lien</a>. Le fichier CSV proposé chiffre le nombre de scrutins auxquels chaque député a participé ou s'est fait représenter au cours de la session écoulée, en plus d'informations complémentaires relatives au mandat de chacun.</p>
