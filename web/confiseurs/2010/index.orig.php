<h1>Étude d'application du règlement relatif à la présence des députés<br/><span style="color: red; font-size: 0.9em; opacity:0.8;">Session 2009&nbsp;&mdash;&nbsp;2010</span></h1>
<div class="etude">
<div style="border: 1px solid red; padding: 10px;">
<p style="color: red;">Contrairement aux affirmations du président Accoyer qui ont été reprises par certains journaux, notre étude prend bien en compte les excuses des députés prévues par le règlement et le Bureau de l'Assemblée : responsabilité dans une assemblée internationale, mission auprès du gouvernement, présence dans une autre commission ou mission d'information, ... Tous ces éléments sont décrits dans la section <i>règlement</i> de l'étude.<br/></p>
<p style="text-align: center;"><strong><a href="http://www.regardscitoyens.org/reaction-au-communique-du-president-accoyer-sur-notre-etude/">Lire notre réaction au communiqué du président de l'Assemblée sur cette étude</a></strong></p>
