<p>Comme <a href="/confiseurs/2009/">en janvier dernier</a>, le collectif <a href="http://www.RegardsCitoyens.org">Regards Citoyens</a> a mené une étude à partir des données de NosDeputes.fr visant à évaluer l'incidence des modifications du règlement sur la participation des députés aux travaux de l'Assemblée, notamment aux réunions des commissions. Le règlement impose en effet avec ses articles 42 et 159 des sanctions financières sur l'indemnité de fonction des députés manifestant des absences répétées aux réunions de leur commission permanente le mercredi matin ou aux scrutins solennels en hémicycle.</p>
<p>La somme de ces sanctions représenterait <strong>243&nbsp;737,33&nbsp;€ d'économie possible</strong> pour l'Assemblée.</p>
