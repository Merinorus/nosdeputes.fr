<a name="stats"></a><h3>Statistiques sur la présence</h3>
<p>En comparant la participation des députés aux travaux des commissions le mercredi matin et le reste du temps au cours de la session 2009 – 2010 écoulée avec celles des sessions précédentes, notre étude révèle une nette augmentation de l'assiduité aux réunions de commission depuis l'application du nouveau règlement.</p>
<p>Le nombre moyen de députés présents aux réunions des commissions permanentes le mercredi matin a ainsi doublé depuis le début de la législature, passant d'environ 23 à 49. Une légère progression de 19 à 22 présents par séance est également constatable pour les réunions ayant lieu à un autre moment de la semaine.</p>
<p style="text-align:center;"><img src="presence-moyenne.png" title="Présence moyenne des députés en commissions permanentes" alt="Présence moyenne des députés en commissions permanentes"/></p>
<p>De la même façon, le nombre moyen de réunions de commission auxquelles assiste un député augmente radicalement au cours de la dernière session, passant pour le mercredi matin d'environ 7 pour les sessions précéntes à 19 cette année. Une progression moins importante mais également très nette se fait également jour en dehors du mercredi matin, passant d'environ 12 à 18 sur la durée de la session ordinaire.</p>
<p style="text-align:center;"><img src="reunions-moyennes.png" title="Participation moyenne des députés aux commissions" alt="Participation moyenne des députés aux commissions"/></p>
<a name="mercredi_reglement"></a><h3>Règlement</h3>
<p>Sanction individuelle d'un député pour chaque absence supplémentaire au delà de deux absences par mois dans sa commission permanente du mercredi matin (<a href="http://www.assemblee-nationale.fr/connaissance/reglement.asp#P719_63307">article 42-3 du règlement</a>) à l'exception des députés&nbsp;:</p>
<ul>
     <li>membres du Bureau de l’Assemblée, à l’exception des secrétaires&nbsp;;</li>
     <li>présidents de groupes&nbsp;;</li>
     <li>élus dans une circonscription située hors de métropole et de Corse.</li>
</ul>
<p>Les députés ne sont pas soumis à ces sanctions s'ils ont une réunion au même moment dans&nbsp;:</p>
<ul>
     <li>une des assemblées internationales ou européennes (APF, APCE, UEO, UIP, APM, APO, ...)&nbsp;;</li>
     <li>une commission spéciale.</li>
</ul>
<p>Enfin, le bureau de l'Assemblée nationale a décidé en début de session que les motifs suivants étaient également jugés valables&nbsp;:</p>
<ul>
<li>maladie, accident ou événement familial grave empêchant le parlementaire de se déplacer&nbsp;;</li>
<li>mission temporaire confiée par le gouvernement&nbsp;;</li>
<li>service militaire&nbsp;;</li>
<li>empêchement insurmontable.</li>
</ul>
<p>Pour plus de détails, le président de la commission des affaires étrangères a précisé ces critères lors d'<a href="/seance/2732#inter_0a57f02fb0cb76347939808c52f0e77f">une intervention le 21 octobre 2009</a>.</p>
<p>Les sanctions encourues sont calculées sur une base de 353,24&nbsp;€ par absence (au delà de la deuxième absence chaque mois). Cette somme correspond à 25&nbsp;% de l'indemnité de fonction d'un député, qui s'élève à 1&nbsp;412,97&nbsp;€ depuis le 1er octobre 2009.</p>
<p>Cette règle est entrée en application en octobre 2009 puis repoussée à décembre après une <a href="http://www.regardscitoyens.org/absences-en-commissions-accoyer-confirme-les-sanctions/">annonce de la présidence en janvier</a>.</p>
